    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed
        // TODO add your handling code here:
        String username ,pass;
        username = txtUserName.getText();
        pass = txtPassWord.getText();
         loginDAO dao = new loginDAO();
        if (jComboBox1.getSelectedItem() == "Admin") {
             admin ad = new admin();
             ad.setUsername(username);
             ad.setPassword(pass);
             int lv = dao.LVTK(ad);
        boolean check = dao.CheckLoginAdmin(ad);
        if (check==true && jComboBox1.getSelectedItem() == "Admin") {
            run.tk =  dao.GetAdmin(username,pass);
            this.setVisible(false);
            run.QLCF();
        }else{
            JOptionPane.showMessageDialog(null, "Tài khoản hoặc mật khẩu không đúng", "Login Error", JOptionPane.ERROR_MESSAGE);
        }  
        }else{
            nhanvien nv = new nhanvien();
            nv.setUsername(username);
            nv.setPassword(pass);
          
             boolean check = dao.CheckLoginNhanvien(nv);
             if (check==true && jComboBox1.getSelectedItem() == "Nhân Viên") {
            run.tk =  dao.GetNhanvien(username, pass);
            this.setVisible(false);
            run.QLCF();
        }else{
            JOptionPane.showMessageDialog(null, "Tài khoản hoặc mật khẩu không đúng", "Login Error", JOptionPane.ERROR_MESSAGE);
        }
        }
        
    }//GEN-LAST:event_btnLogInActionPerformed
